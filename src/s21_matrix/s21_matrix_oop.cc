/**
 * @brief
 * Оператор присваивания копированием
 *
 * @details
 * Одна из целей разработки C++ заключается в том, что определяемые
 * пользователем типы (классы) должны вести себя так же, как и встроенные типы.
 * Должна быть возможность назначить два объекта одного типа. Поскольку все
 * поведения в классах определены как функции, должна быть функция оператора
 * присваивания.
 *
 * Если в программе объявить класс, в котором оператор присваивания не
 * перегружается, то для этого класса компилятором будет создан оператор
 * присваивания по умолчанию. При вызове оператора присваивания по умолчанию
 * происходит побайтовое копирование одного экземпляра класса другому.
 *
 * В нашем случае это недопустимо, т.к. в этом случае 2 экземпляра S21Matrix в
 * поле matrix_ будут указывать на один и тот же участок памяти - при удалении
 * одного из объектов, второй потеряет все данные и станет неконсистентным.
 *
 * Вызывать Free(), а потом копировать данные other - ошибка, мы не соблюдаем
 * строгую гарантию безопасности исключений. Поэтому используем временный объект
 * для копии. Затем, т.к. матрица copy все равно будет уничтожена при выходе из
 * функции, используем перемещение (что бесплатно) вместо копирования (что
 * дорого)
 *
 * @param other копируемый объект S21Matrix
 * @return S21Matrix&
 */
S21Matrix &S21Matrix::operator=(const S21Matrix &other) {
  // Философский вопрос - нужна ли проверка if (this != &other)
  // Если бы мы очищали текущую матрицу перед копированием данных, то точно
  // нужна была бы (иначе мы бы очищали other при самоприсваивании).
  // В такой реализации (copy and swap фактически) ничего страшного не
  // произойдет при самокопировании, кроме того, что мы создали копию данных,
  // которую можно было не создавать. А стоит ли 100000 раз делать проверку с
  // отрицательным результатом, чтобы один раз не сделать копирование - что
  // статистически займёт больше времени?
  S21Matrix copy{other};
  *this = std::move(copy);
  return *this;
}

/**
 * @brief
 * Оператор присваивания перемещением.
 *
 * @details
 * Цель использования оператора перемещения такая же, как и конструктора
 * перемещения – ускорить выполнение программы за счет избежания
 * непосредственного копирования данных при присваивании так называемых
 * rvalue-ссылок, которые используются в выражениях в правой части оператора
 * присваивания (это если по-простому, вообще понятие rvalue/lvalue/xvalue
 * обширнее).
 *
 * Если в классе не реализован оператор перемещения, то этот оператор заменяется
 * оператором копирования.
 *
 * В общем случае оператор состоит из следующих действий:
 * 1) Проверка, не происходит ли присваивание экземпляра самому себе в случаях,
 * когда метод может каким-либо образом возвращать этот же экземпляр (Кроме
 * того, что это защита от дурака, некоторые алгоритмы, например, std::sort,
 * могут выполнять такие назначения)
 * 2) Освобождение памяти под выделенные внутренние данные. Экземпляр lvalue уже
 * создан ранее и в нем уже есть некоторые данные;
 * 3) Присваивание внутренним указателям адресов данных, которые необходимо
 * скопировать в текущий экземпляр.
 *
 * Судьба other при этом нам безразлична, но процитируем стандарт:
 *
 * Objects of types defined in the C++ standard library may be moved from
 * ([class.copy]). Move operations may be explicitly specified or implicitly
 * generated. Unless otherwise specified, such moved-from objects shall be
 * placed in a valid but unspecified state.
 *
 * Т.е. после перемещения other находится в корректном, но неопределенном
 * состоянии. Поэтому допустимым вариантом является ничего в this не чистить, а
 * обменяться ресурсами с other (см. комментарий в теле функции). Оставлять
 * other в некорректном (неконсистентном) состоянии - ошибка.
 *
 * @param other объект S21Matrix, у которого забираем владение ресурсами
 * @return S21Matrix&
 */
S21Matrix &S21Matrix::operator=(S21Matrix &&other) noexcept {
  if (this != &other) {
    // Можно убрать вообще Free() и делать только swap
    // В этом случае удаление сваливаем на деструктор объекта other, который
    // всё равно будет вызван в ближайшее время. А нужна ли проверка на
    // самоприсваивание, если убрать Free()?
    Free();

    std::swap(rows_, other.rows_);
    std::swap(cols_, other.cols_);
    std::swap(matrix_, other.matrix_);
  }

  return *this;
}

