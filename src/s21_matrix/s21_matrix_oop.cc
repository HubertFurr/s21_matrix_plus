/**
 * @brief
 * Конструктор по умолчанию (Default constructor). Создает объект S21Matrix
 * размером [0 × 0], заполненный нулями.
 *
 * @details
 * Выносим объявление полей в Member Initializer List (MIL), чтобы избежать
 * двойной инициализации (да и в целом рекомендуемая практика - всегда, если
 * возможно, использовать MIL).
 *
 * noexcept - указывает, что наш метод не генерирует (не выбрасывает)
 * исключения и не завершает аварийно свою работу. Т.е. вы не бросаете
 * исключение в коде метода, а все используемые внутри методы тоже не бросают
 * исключения (т.е. являются noexcept). Либо всё, что может бросить исключение,
 * обернуто в try / catch и мы гарантируем, что исключение не будет проброшено
 * выше.
 *
 * noexcept сильно уменьшает размер итогового бинарного файла и ускоряет работу
 * программы. Но использовать его необходимо осторожно, т.к. если метод,
 * помеченный noexcept, выпустит исключение наружу, то ваша программа вызовет
 * std::terminate() и завершится, даже не соблаговолив вызвать деструкторы для
 * уже созданных переменных (стек НЕ обязан развернуться).
 *
 * Распространенным заблуждением является, что обработка исключений очень очень
 * медленная и стоит отказаться от исключений и всё делать noexcept. Некоторые
 * даже утверждают, что Google Style запрещает использовать исключения. Но:
 * 1) В Google style написано "the benefits of using exceptions outweigh the
 * costs, especially in new projects" (но вообще рекомендую прочитать весь блок
 * пр исключения там)
 * 2) Есть случаи, когда нет никаких альтернатив (например, конструкторы)
 * 3) Основная используемая сейчас модель исключений - это модель исключений с
 * нулевой стоимостью (это бесплатно и быстрее явных проверок if(error), когда
 * не происходит никаких исключений).
 * 4) Есть, конечно, много нюансов, но упрощенно ситуация такая.
 *
 * Почему по умолчанию [0 × 0]? Самый распространенный сценарий использования
 * дефолтного конструктора - заранее создать переменную, значение которой будет
 * в ходе какого-то алгоритма заменено результатами расчетов через присваивание.
 * При этом объект будет очищен и память будет перевыделена - поэтому нет смысла
 * сразу выделять какое-то количество памяти, которая все равно буде освобождена
 * в будущем. Нет никаких логических причин, почему по умолчанию матрица
 * должна быть [3 × 3], а не [1 × 1] или [5 × 5]. В случае специализированных
 * матриц дефолтное значение вполне может быть определенно заранее, например,
 * класс S21Matrix4x4 для 3d-графики можно сразу создавать [4 × 4].
 *
 * Кроме того, матрица [0 × 0] должна существовать из-за реализации конструктора
 * перемещения (см. описание конструктора перемещения). А еще есть концепция,
 * что дефолтный конструктор должен создавать "пустой" объект (см. класс
 * SimpleMatrix в данном репозитории)
 */
S21Matrix::S21Matrix() noexcept : rows_(0), cols_(0), matrix_(nullptr) {}

